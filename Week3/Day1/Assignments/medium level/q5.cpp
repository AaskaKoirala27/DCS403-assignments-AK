// Factorial of a Number (Using `for` loop)

#include <iostream>
using namespace std;

int main() {
    int n;
    long long fact = 1;

    cout << "Euta number halnu hos: ";
    cin >> n;

    for (int i = 1; i <= n; i++) {
        fact = fact * i;
    }

    cout << "Factorial of " << n << " is: " << fact << endl;

    return 0;
}
